package ru.strukovd.eng.enums;

public enum PartOfSpeech {
    NOUN,             // существительное
    VERB,             // глагол
    ADJECTIVE,        // прилагательное
    ADVERB,           // наречие
    PRONOUN,          // местоимение
    PREPOSITION,      // предлог
    CONJUNCTION,      // союз
    INTERJECTION,     // междометие
    ARTICLE,          // артикль
    NUMERAL,          // числительное
    PARTICLE,         // частица
    DETERMINER,       // определитель (например: this, that)
    PHRASE,           // фраза (если нужно описывать устойчивые выражения)
    OTHER             // для редких или не классифицированных случаев
}